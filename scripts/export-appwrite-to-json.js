/**
 * export-appwrite-to-json.js
 * Exports Appwrite collections to JSON files for InsForge bulk import.
 * Handles ID generation (UUID) and FK mapping.
 */
const { Client, Databases, Query } = require('node-appwrite');
const fs = require('fs');
const crypto = require('crypto');
require('dotenv').config();

const APPWRITE_ENDPOINT = process.env.APPWRITE_ENDPOINT;
const APPWRITE_PROJECT = process.env.APPWRITE_PROJECT_ID;
const APPWRITE_KEY = process.env.APPWRITE_API_KEY;
const DATABASE_ID = 'finaria';

const appwrite = new Client()
    .setEndpoint(APPWRITE_ENDPOINT)
    .setProject(APPWRITE_PROJECT)
    .setKey(APPWRITE_KEY);

const db = new Databases(appwrite);

const SCHEMA = {
    organizations: ['id', 'name', 'slug', 'linking_code', 'created_at'],
    profiles: ['id', 'full_name', 'org_id', 'role', 'start_date', 'avatar_url', 'created_at', 'updated_at'],
    org_members: ['id', 'org_id', 'user_id', 'role_in_org', 'status', 'created_at'],
    lessons: ['day', 'title', 'summary', 'mission', 'audio_url'],
    user_lesson_progress: ['id', 'user_id', 'lesson_id', 'day', 'org_id', 'unlocked_at', 'completed_at', 'notes'],
    accounts: ['id', 'user_id', 'org_id', 'name', 'type', 'currency', 'opening_balance', 'cut_off_day', 'payment_day', 'credit_limit', 'created_at'],
    categories: ['id', 'org_id', 'user_id', 'kind', 'name', 'parent_id', 'is_default', 'icon', 'color', 'created_at'],
    transactions: ['id', 'org_id', 'user_id', 'account_id', 'kind', 'amount', 'occurred_at', 'category_id', 'note', 'transfer_account_id', 'is_recurring', 'recurrence_period', 'recurrence_day_of_month', 'recurrence_interval_months', 'recurrence_total_occurrences', 'expense_label', 'is_scheduled', 'created_at'],
    budgets: ['id', 'org_id', 'user_id', 'month', 'name', 'created_at'],
    budget_items: ['id', 'budget_id', 'category_id', 'limit_amount', 'created_at'],
    points_rules: ['key', 'points', 'is_active'],
    points_events: ['id', 'org_id', 'user_id', 'event_key', 'points', 'ref_table', 'ref_id', 'created_at'],
    points_totals: ['org_id', 'user_id', 'total_points', 'updated_at'],
    savings_goals: ['id', 'account_id', 'name', 'target_amount', 'target_date', 'created_at', 'updated_at'],
    physical_assets: ['id', 'user_id', 'org_id', 'name', 'amount', 'created_at'],
    pilot_daily_recommendations: ['id', 'user_id', 'org_id', 'recommendation_date', 'state', 'message_main', 'message_why', 'suggested_limit', 'suggested_action', 'flexibility', 'signals_snapshot', 'created_at'],
    pilot_emotional_checkins: ['id', 'user_id', 'checkin_date', 'value', 'created_at']
};

const ORDERED_COLLECTIONS = [
    'organizations',
    'profiles',
    'org_members',
    'lessons',
    'user_lesson_progress',
    'accounts',
    'categories',
    'transactions',
    'budgets',
    'budget_items',
    'points_rules',
    'points_events',
    'points_totals',
    'savings_goals',
    'physical_assets',
    'pilot_daily_recommendations',
    'pilot_emotional_checkins'
];

// Map OldID -> NewUUID
let ID_MAP = {};
// Map Day -> Generated Lesson ID (although lessons relies on serial usually, bulk insert might strict types)
// If bulk insert allows omitting ID for serial, good.
// But we need to know the ID for relations. 
// Issue: Bulk insert via MCP returns what? 
// If it doesn't return IDs, we are stuck for 'user_lesson_progress'.
// Solution: For 'lessons', we MIGRATE them with explicit IDs if possible?
// ID in Postgres is SERIAL (1, 2, 3...). 
// We can't easily force 1, 2, 3 unless we reset valid sequence.
// ALTERNATIVE: For lessons, we assume they are static and maybe don't migrate user progress?
// Or we assume 'day' is the key? 
// user_lesson_progress uses lesson_id.
// If we can't get the new ID, we can't link.
// Wait, 'lessons' table is static content. 
// Maybe we can query InsForge FIRST to build the map?
// Yes. We can use mcp_insforge to get lessons? No, that's manual.
// We can assume lessons are seeded 1..30.
// We can map `lesson.day` to `id` (e.g. id = day).
// If `lessons` are inserted in order day 1..N, IDs will be 1..N.
// Let's assume ID = Day.
// For other tables, UUIDs are generated by us, so we KNOW them.

function genUUID() {
    return crypto.randomUUID();
}

async function fetchAllDocuments(collectionName) {
    let cursor = null;
    let allDocs = [];
    while (true) {
        const queries = [Query.limit(100)];
        if (cursor) queries.push(Query.cursorAfter(cursor));
        try {
            const res = await db.listDocuments(DATABASE_ID, collectionName, queries);
            if (res.documents.length === 0) break;
            allDocs.push(...res.documents);
            cursor = res.documents[res.documents.length - 1].$id;
        } catch (e) {
            console.warn(`Failed to list ${collectionName}:`, e.message);
            break;
        }
    }
    return allDocs;
}

async function exportCollection(name) {
    console.log(`Exporting ${name}...`);
    const docs = await fetchAllDocuments(name);
    console.log(`Found ${docs.length} docs in ${name}`);

    const allowedCols = SCHEMA[name];
    if (!allowedCols) return;

    const rows = [];

    for (const doc of docs) {
        const row = {};
        const oldId = doc.$id;
        let newId = genUUID();

        // Special Lesson logic
        if (name === 'lessons') {
            // We assume ID matches Day for simplicity provided we insert in order
            // actually we can't force ID on serial unless we use OVERRIDING SYSTEM VALUE
            // But JSON import might not support that.
            // Let's omit ID and hope order is preserved, OR we rely on day.
            newId = null;
        }

        ID_MAP[oldId] = newId;

        allowedCols.forEach(col => {
            let val = doc[col];

            if (col === 'id') return;

            // FK Mapping
            if ((col.endsWith('_id') || col === 'parent_id') && typeof val === 'string') {
                if (ID_MAP[val]) {
                    val = ID_MAP[val];
                } else if (name === 'user_lesson_progress' && col === 'lesson_id') {
                    // Map via day if possible?
                    // doc.day should exist. 
                    // Assuming LessonID = Day is risky without verify.
                    // But we have no way to get back Serial ID from bulk insert easily.
                    // Let's assume we map explicitly if we can.
                    // If we assign `link_lesson_id` = doc.day? No.
                    val = null; // Risky
                }
            }
            if (val !== undefined) row[col] = val;
        });

        if (name !== 'lessons') {
            row.id = newId;
        } else {
            // For lessons, we map oldID -> null. 
            // user_lesson_progress will fail to map.
            // Workaround: We don't migrate user_lesson_progress lesson_id correctly?
            // Or we rely on 'day' being present in user_lesson_progress and backend trigger fixes it?
        }

        if (allowedCols.includes('created_at')) row.created_at = doc.$createdAt;
        if (allowedCols.includes('updated_at')) row.updated_at = doc.$updatedAt;

        rows.push(row);
    }

    if (rows.length > 0) {
        fs.writeFileSync(`${name}.json`, JSON.stringify(rows, null, 2));
        console.log(`Saved ${name}.json (${rows.length} records)`);
    }
}

async function main() {
    try {
        for (const col of ORDERED_COLLECTIONS) {
            await exportCollection(col);
        }
        console.log('Export finished.');
    } catch (e) {
        console.error('Fatal export error:', e);
    }
}

main();
